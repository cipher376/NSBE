						<p ><ul><li><h4><b>Annual Leadership Conference</b></h4></li></ul>
								Pre Collegiate Initiative (<b>PCI</b>) Conference for Senior High Schools. The PCI programs are established to stimulate technical interest and development of Junior and Senior high schools students. The Pre-College Initiative (PCI) Program is designed to stimulate the interest in science, technology, engineering, and mathematics fields, or STEM. The goal is to encourage students in grades K–12 to attend college and pursue technical degrees. 

								Our PCI program provides activities to help students discover firsthand how engineering and technology relate to the world around them and discover the excitement of academic excellence, leadership, technical development and teamwork.  Our PCI Committee prepares programs for our NSBE-Jr. Chapters that offer our youth academic support while incorporating social support. During PCI Conferences, students are provided with an opportunity to visit a University or Polytechnic to experience and participate in various college preparatory workshops. As part of our PCI programs, Educational trips to Companies in the country are made. This allows students to visit companies that have assisted NSBE one way or the other, to learn the engineering part of workforce at these companies.

						</p>

						<p ><ul><li><h4><b>Women in Science and Engineering (WISE) </b></h4></li></ul>
								The WISE Committee seeks to encourage Girls and Women in our Society to embrace the learning of science at all levels of the educational system. This help created a good proportion of women involved in the learning, teaching and practice of science, engineering, technology and related fields as compared to their male Counterparts.  Encouraging girls to specialize in science, mathematics and engineering at the secondary and higher echelons of the education system is our hallmark. 

						</p>
						<p ><ul><li><h4><b>Vacation Engineering Experience for Kids (VEEK).</b></h4></li></ul>
								 The VEEK is a Free Two to three-week program designed to be a engaging educational experience, led by NSBE engineering students and technical professionals dedicated to pursuing professional excellence and giving back to the community. The VEEK Program utilizes a hands-on design curriculum supplied by various Educational Curriculum companies and Cooperate Sponsors. Campers will work in teams, using their knowledge to solve problems and create products while discovering the underlying math and science principles involved in these processes. Each week, the campers will take on a new project, culminating with a presentation, design competition and physical competitions that all parents are encouraged to attend.

